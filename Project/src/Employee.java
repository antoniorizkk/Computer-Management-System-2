
public class Employee {
	String name,salary,id;

	public Employee(String name, String salary, String id) {
		super();
		this.name = name;
		this.salary = salary;
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public String getSalary() {
		return salary;
	}

	public String getId() {
		return id;
	}
	
}
